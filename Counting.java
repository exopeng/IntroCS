public class Counting {
	public static void main(String[] args) {
		//1a
		for ( int i = 1; i < 26;i++) {
			System.out.println(i);
		}
		System.out.println();
		
		//1b
		for (int i = -5; i < 11; i++) {
			System.out.println(i);
	
		}
		System.out.println();
		
		//1c
		for (int i = 30 ; i < 99; i += 7) {
			System.out.println(i);
		
		}
		System.out.println();
		
		//1d
		for (int i = 10; i > -1;i -= 1) {
			System.out.println(i);
		
		}
		System.out.println();
		
		//1e
		for (int i = 1;i < 26;i++) {
			System.out.println(i * i);
		
		}
		System.out.println();
		
		//1f
		for (int i = 1;i < 6;i++) {
			for (int j = 1;j < 5; j++) {
				System.out.println(i + "x" + j + "=" + i * j) ;
			}
		}
		System.out.println();
		
		//2a
		for (int i = 0; i < 4; i++ ) {
			System.out.println("*****");
		
		}
		System.out.println();
		
		//2b
		int size = 5;
		for (int i = 0;i <= size; i++) {
			for (int j = 0; j <= size; j++) {
				System.out.print("*");
			
			}
			System.out.println();
		
		}
		System.out.println();
		
		//2c
		size = 10;
		for ( int i = 0; i <= size ; i++) {
			for ( int j = 0; j <= size; j++) {
				System.out.print("*");
			
			}
			System.out.println();
			
		}
		
		
	
	
	
	}
}
































